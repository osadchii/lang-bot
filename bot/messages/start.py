"""Start and help messages in Russian."""

import html


def get_welcome_message(first_name: str, is_new_user: bool) -> str:
    """Get welcome message.

    Args:
        first_name: User's first name
        is_new_user: Whether this is a new user

    Returns:
        Welcome message text
    """
    escaped_name = html.escape(first_name)
    if is_new_user:
        return (
            f"<b>Добро пожаловать в бот для изучения греческого, {escaped_name}!</b>\n\n"
            f"Я помогу тебе учить греческий язык:\n"
            f"- <b>Система интервальных повторений</b> для эффективного запоминания\n"
            f"- <b>ИИ-помощник</b> для переводов и объяснений грамматики\n"
            f"- <b>Отслеживание прогресса</b> для мониторинга успехов\n\n"
            f"Начни с создания первой колоды!"
        )
    return (
        f"<b>С возвращением, {escaped_name}!</b>\n\n"
        f"Готов продолжить изучение греческого?\n"
        f"Выбери действие в меню ниже."
    )


